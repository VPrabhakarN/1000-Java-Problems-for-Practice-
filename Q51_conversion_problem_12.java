// Java program to Convert char to String

public class Q51_conversion_problem_12 {
    public static void main(String args[])
    {
        // Defining char 
        char ch = 'v';
        System.out.println("Char : "+ch);

        // conversion
        String str = String.valueOf(ch);
        System.out.println("String : "+str);
    }
}
