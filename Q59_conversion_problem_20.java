// Java program to Convert double to int

public class Q59_conversion_problem_20 {
    public static void main(String args[])
    {
        // Defining double 
        double d = 1923.1923;
        System.out.println("Double : "+d);

        // conversion 
        int i = (int)d;
        System.out.println("Int : "+i);
    }
}
