// Java program to Convert temperature degree from Celsius to Kelvin
public class Q69_conversion_problem_29 {
    public static void main(String args[])
    {
        // Defining Celsius 
        float cels = 28; // 28 degrees
        System.out.println("Celsius : "+cels);

        // Conversion 
        float kel = cels + 273;
        System.out.println("Kelvin : "+kel);
    }
}
