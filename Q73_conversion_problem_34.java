// Java program to convert cm to feet

public class Q73_conversion_problem_34 {
    public static void main(String args[])
    {
        // Defining cm 
        float cm = 2;
        System.out.println("Centimeters : "+cm);

        // conversion 
        double feet = cm / 30.48;
        System.out.println("Feet : "+feet);
    }
}
