// Java program to convert feet to cm

public class Q81_conversion_problem_42 {
    public static void main(String args[])
    {
        // Defining feet 
        float feet = 12;
        System.out.println("Feet : "+feet);

        // Conversion 
        double cm = feet * 30.48;
        System.out.println("Centimeter : "+cm);
    }
}
