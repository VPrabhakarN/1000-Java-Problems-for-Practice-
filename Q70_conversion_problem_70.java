// Java program to Convert temperature degree from Kelvin to celsius

public class Q70_conversion_problem_70 {
    public static void main(String args[])
    {
        // Defining kelvin 
        float k = 23;
        System.out.println("Kelvin : "+k);

        // Conversion 
        double c = k-273.15;
        System.out.println("Celsius : "+c);

    }
}
