// Java Convert int to char

public class Q42_converson_problem_3 {
    public static void main(String args[])
    {
        // defining an int 
        int n = 1;
        System.out.println("An Int : "+n);

        // convertion int into char 
        char ch = (char) n;
        System.out.println("The Char : "+ch);
    }
}
