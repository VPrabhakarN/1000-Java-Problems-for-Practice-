// Java program to Convert String to char

public class Q49_conversion_problem_10 {
    public static void main(String args[])
    {
        // Defining String 
        String str = "ViTa";
        System.out.println("String : "+str);

        // conversion 
        char ch = str.charAt(2);
        System.out.println("Char : "+ch);

    }
}
