// Java program to Convert hours into seconds

public class Q64_conversion_problem_25 {
    public static void main(String args[])
    {
        // Defining hours 
        float hrs = 3.5F;
        System.out.println("Hours : "+hrs);

        // conversion 
        float secs = hrs*60*60;
        System.out.println("Seconds : "+secs);
    }
}
