// Java program to Convert String to float

public class Q55_conversion_problem_16 {
    public static void main(String args[])
    {
        // Defining string 
        String str = "19.23";
        System.out.println("String : "+str);

        // conversion 
        float f = Float.parseFloat(str);
        System.out.println("Float : "+f);
    }
}
