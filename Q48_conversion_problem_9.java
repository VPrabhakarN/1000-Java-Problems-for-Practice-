// Java program to Convert String to double
public class Q48_conversion_problem_9 {
    public static void main(String args[])
    {
        // Defing string 
        String str = "19.23";
        System.out.println("String : "+str);

        // conversion 
        double d = Double.parseDouble(str);
        System.out.println("Double : "+d);
    }
}
