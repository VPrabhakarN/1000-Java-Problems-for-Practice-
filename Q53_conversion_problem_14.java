// Java Program to convert Fahrenheit into Celsius

public class Q53_conversion_problem_14 {
    public static void main(String args[])
    {
        // Defining Fahreneit 
        float Fahreneit = 101;
        System.out.println("Fahrenheit : "+Fahreneit);

        // Conversion 
        float celsius = ((Fahreneit-32)*5)/9;
        System.out.println("Celsius : "+celsius);
    }
}
