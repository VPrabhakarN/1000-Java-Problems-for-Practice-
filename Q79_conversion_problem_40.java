// Java program to convert Inches to Feet

public class Q79_conversion_problem_40 {
    public static void main(String args[])
    {
        // Defining inches 
        float inches = 19;
        System.out.println("Inches : "+inches);

        // conversion 
        double feet = inches / 12;
        System.out.println("Feet : "+feet);
    }
}
