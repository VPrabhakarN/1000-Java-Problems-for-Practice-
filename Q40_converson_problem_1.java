// Java program to Convert String to int

public class Q40_converson_problem_1 {
    public static void main(String args[])
    {
        // defining a string 
        String str = "5";
        System.out.println("The String : "+str);

        // conversion 
        int n = Integer.parseInt(str);
        System.out.println("The int : "+n);
    }
}