// Java program to convert mm to cm

public class Q71_conversion_problem_32 {
    public static void main(String args[])
    {
        // Defining mm 
        float mm = 11.12F;
        System.out.println("Millimeter : "+mm);

        // conversion 
        float cm = mm/10;
        System.out.println("Centimeter : "+cm);
    }
}
