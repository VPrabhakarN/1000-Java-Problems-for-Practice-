// Java program to convert feet to inches

public class Q83_conversion_problem_44 {
    public static void main(String args[])
    {
        // Defining feet 
        float feet = 23;
        System.out.println("Feet : "+feet);

        // Conversion 
        double inches = feet * 12;
        System.out.println("Inches : "+inches);
    }
}
